    def test_settings_screen_gather(self):
        """Test gathering settings from widgets."""
        current_settings = {
            "concurrency": 16,
            "per_host": 8,
            "respect_robots": True,
            "verify_ssl": True,
            "debug_panel_enabled": False,
            "cache_enabled": True,
            "custom_headers": {},
        }
        screen = SettingsScreen(current_settings)
        # Mock the query_one to return appropriate "widgets" without using real Textual widgets
        def mock_query_one(selector):
            if "concurrency" in selector:
                widget = MagicMock()
                widget.value = "32"
                return widget
            elif "per_host" in selector:
                widget = MagicMock()
                widget.value = "16"
                return widget
            elif "respect_robots" in selector:
                widget = MagicMock()
                widget.value = False
                return widget
            elif "custom_headers" in selector:
                widget = MagicMock()
                widget.text = '{"User-Agent": "test"}'
                return widget
            else:
                widget = MagicMock()
                widget.value = True
                return widget

        screen.query_one = mock_query_one

        gathered = screen._gather()
        assert "concurrency" in gathered
        assert "per_host" in gathered

    def test_settings_screen_validate(self):
        """Test validation of settings."""
        current_settings = {
            "concurrency": 16,
            "per_host": 8,
            "respect_robots": True,
            "verify_ssl": True,
            "debug_panel_enabled": False,
            "cache_enabled": True,
            "custom_headers": {},
        }
        screen = SettingsScreen(current_settings)
        
        # Valid settings
        valid_data = {
            "concurrency": 16,
            "per_host": 8,
            "respect_robots": True,
            "verify_ssl": True,
            "debug_panel_enabled": False,
            "cache_enabled": True,
            "custom_headers": {},
        }
        error = screen._validate(valid_data)
        assert error is None
        
        # Invalid settings
        invalid_data = {"concurrency": 300}  # Too high
        error = screen._validate(invalid_data)
        assert error is not None

    def test_settings_screen_coerce_custom_headers(self):
        """Test coercion of custom headers from string to dict."""
        current_settings = {"custom_headers": {}}
        screen = SettingsScreen(current_settings)
        
        data = {"custom_headers": '{"User-Agent": "test"}'}
        screen._coerce_custom_headers(data)
        assert isinstance(data["custom_headers"], dict)
        assert data["custom_headers"] == {"User-Agent": "test"}
        
        # Empty string should become empty dict
        data = {"custom_headers": ""}
        screen._coerce_custom_headers(data)
        assert data["custom_headers"] == {}

