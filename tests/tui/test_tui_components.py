@asynccontextmanager
async def run_widget_app(widget: Any) -> tuple[Any, Any]:
    """Run a single widget inside a minimal Textual App for testing."""

    class _WidgetApp(App):
        def compose(self) -> ComposeResult:  # type: ignore[override]
            yield widget

    app = _WidgetApp()
    async with app.run_test() as pilot:  # type: ignore[attr-defined]
        yield widget, pilot


