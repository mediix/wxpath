from wxpath.core.runtime import engine


class TestEngineExceptionHandling:
    """Test exception handling in WXPathEngine."""

    def test_yield_errors_parameter_exists(self):
        """Test that WXPathEngine accepts yield_errors parameter."""
        eng = engine.WXPathEngine(yield_errors=False)
        assert eng.yield_errors is False
        
        eng = engine.WXPathEngine(yield_errors=True)
        assert eng.yield_errors is True

    def test_safe_iterator_function_exists(self):
        """Test that _safe_iterator function exists and is callable."""
        assert hasattr(engine, "_safe_iterator")
        assert callable(engine._safe_iterator)

    def test_run_accepts_yield_errors_parameter(self):
        """Test that run() method accepts yield_errors parameter."""
        # This is a simple test to verify the API exists
        # Full integration tests would require complex mocking
        eng = engine.WXPathEngine()
        # Verify the method signature accepts yield_errors
        import inspect
        sig = inspect.signature(eng.run)
        assert "yield_errors" in sig.parameters
