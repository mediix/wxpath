    def test_wx_functions_with_context_work(self):
        """Test that wx: functions work correctly with proper context."""
        html_str = "<html><body><p>Test</p></body></html>"
        root = html.fromstring(html_str, parser=html_parser_with_xpath3)
        root.set("depth", "3")
        root.base_url = "http://example.com"
        
        # With "/" prefix, should work
        result = root.xpath3("/wx:depth()")
        assert result == 3
        
        result = root.xpath3("/wx:current-url()")
        assert "example.com" in str(result[0])

