"""Additional tests for XPathContextRequired exception in patches.

Documentation generated by LLM - requires human review
pre-1.0.0 - APIs and contracts may change
"""

import pytest
from lxml import html

from wxpath.patches import XPathContextRequired, html_parser_with_xpath3


class TestXPathContextRequired:
    """Test XPathContextRequired exception."""

    def test_exception_message(self):
        """Test that exception has informative message."""
        error = XPathContextRequired()
        assert "XPathContext" in str(error)
        assert "required" in str(error).lower()

    def test_wx_depth_without_context(self):
        """Test wx:depth() raises XPathContextRequired without context."""
        html_str = "<html><body><p>Test</p></body></html>"
        root = html.fromstring(html_str, parser=html_parser_with_xpath3)
        
        # Calling wx:depth() without a preceding "/" should raise XPathContextRequired
        with pytest.raises(XPathContextRequired):
            root.xpath3("wx:depth()")

